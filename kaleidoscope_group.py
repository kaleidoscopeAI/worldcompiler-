def _demo() -> None:
    seed = 0
    dim = 6
    group = KaleidoscopeGroup(dim=dim, fold=6)

    print("=" * 70)
    print("KALEIDOSCOPE GROUP — strains as orbits, supernodes as invariants")
    print("=" * 70)
    print(f"  latent dim {dim}, {group.fold}-fold, |G| = "
          f"{group.elements.shape[0]} turns+mirrors")

    gate_orbit_invariance(group, seed=seed)
    print("GATE orbit-invariance      PASS  (one invariant per orbit, all views "
          "agree)")

    same, far = gate_orbit_beats_distance(seed=seed, dim=dim)
    print(f"GATE orbit-beats-distance  PASS  (mirrored-far view unified into one "
          f"strain: same={same}, far={far})")

    # Build codes that are mixtures of a few 'shapes' plus their mirrored views.
    rng = core.derive_rng("kg-demo", seed)
    base_shapes = rng.normal(size=(4, dim))
    codes = []
    for _ in range(200):
        shape = base_shapes[rng.integers(4)]
        g = group.elements[rng.integers(group.elements.shape[0])]  # random view
        codes.append(g @ shape + 0.05 * rng.normal(size=dim))
    codes = np.stack(codes)

    gate_determinism(codes, group)
    print("GATE determinism           PASS  (identical strain labels x2)")

    strains, labels = form_strains(codes, group, resolution=0.35)
    s_star = one_entity(strains)

    print("-" * 70)
    print("STRAINS FROM 200 CODES (4 hidden shapes × random turns+mirrors)")
    print(f"  strains discovered           : {len(strains)}  "
          f"(hidden shapes: 4)")
    print(f"  each strain embodies         : {group.elements.shape[0]} views "
          f"(its full orbit)")
    print(f"  S* one-entity (Σ mass·view)  : dim={s_star.shape[0]}, "
          f"‖S*‖={np.linalg.norm(s_star):.4f}")
    print("-" * 70)
    for i, s in enumerate(sorted(strains, key=lambda x: -x.mass)[:6]):
        print(f"  strain {i}: mass {s.mass:.3f}  members {len(s.members):3d}  "
              f"‖invariant‖ {np.linalg.norm(s.signature):.3f}  "
              f"rep[:3]={np.round(s.representative[:3], 3).tolist()}")


if __name__ == "__main__":
    _demo()
