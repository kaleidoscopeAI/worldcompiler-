# Gates
# ===========================================================================


def gate_orbit_invariance(group: KaleidoscopeGroup, trials: int = 20,
                          seed: int = 0, tol: float = 1e-6) -> None:
    """The defining property: every member of an orbit must share one invariant
    signature. If applying any turn/mirror changed the signature, the invariant
    would not identify the kaleidoscope image and the whole construction is
    void."""
    rng = core.derive_rng("orbit-inv-gate", seed, group.dim)
    for _ in range(trials):
        z = rng.normal(size=group.dim)
        base = group.invariant(z)
        orbit_pts = group.orbit(z)
        for p in orbit_pts:
            dev = float(np.max(np.abs(group.invariant(p) - base)))
            if dev > tol:
                raise AssertionError(
                    f"orbit-invariance gate FAILED: signature drifted {dev:.2e} "
                    f"under a group element (invariant is not invariant)")


def gate_orbit_beats_distance(seed: int = 0, dim: int = 6) -> Tuple[bool, bool]:
    """The falsifiable difference from clustering. Take a code, produce a
    mirrored-rotated view of it that is FAR in raw distance. The kaleidoscope
    must put them in the SAME strain (they are the same image); a nearest-
    centroid / distance rule would put them in DIFFERENT clusters. We assert the
    kaleidoscope unifies them AND that they are genuinely distance-separated
    (so the test is non-trivial)."""
    group = KaleidoscopeGroup(dim=dim, fold=6)
    rng = core.derive_rng("orbit-vs-dist-gate", seed, dim)
    z = rng.normal(size=dim)
    # A non-identity group element gives a mirrored/rotated view.
    g = group.elements[len(group.elements) // 2]
    z_view = g @ z
    raw_distance = float(np.linalg.norm(z - z_view))

    codes = np.stack([z, z_view])
    strains, labels = form_strains(codes, group, resolution=0.35)
    same_strain = bool(labels[0] == labels[1])
    genuinely_far = raw_distance > 0.5 * np.linalg.norm(z)

    if not same_strain:
        raise AssertionError(
            "orbit-vs-distance gate FAILED: a mirrored view was NOT unified "
            "into the same strain (engine is behaving like clustering)")
    if not genuinely_far:
        raise AssertionError(
            "orbit-vs-distance gate FAILED: the mirrored view was not actually "
            "distance-separated, so the test is trivial")
    return same_strain, genuinely_far


def gate_determinism(codes: np.ndarray, group: KaleidoscopeGroup) -> None:
    a, la = form_strains(codes.copy(), group)
    b, lb = form_strains(codes.copy(), group)
    if not np.array_equal(la, lb):
        raise AssertionError("determinism gate FAILED: strain labels differ")


